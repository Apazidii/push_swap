#include "push_swap.h"

int	is_numbers(char *s)
{
	while (*s != '\0')
	{
		if (!((*s >= '0' && *s <= '9') || *s == ' '))
			return (0);
		s++;
	}
	return (1);
}

void	free_split(char **arr)
{
	char	**buf;

	while (*arr)
	{
		buf = arr + 1;
		free(arr);
		arr = buf;
	}
}
